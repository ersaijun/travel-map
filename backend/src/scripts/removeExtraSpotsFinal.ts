async function removeExtraSpots() {
  try {
    await mongoose.connect(process.env.MONGODB_URI || 'mongodb://localhost:27017/travel-map');
    console.log('MongoDB 连接成功');

    const allSpots = await ScenicSpot.find({}).sort({ name: 1 });
    console.log(`\n数据库中共有 ${allSpots.length} 个景区`);
    console.log(`官方5A级景区名单有 ${official5ASpots.length} 个\n`);

    // 找出需要删除的景区
    const toDelete = [];
    for (const spot of allSpots) {
      if (spot.name && !official5ASpots.includes(spot.name)) {
        toDelete.push(spot);
      }
    }

    console.log(`需要保留的5A级景区: ${allSpots.length - toDelete.length} 个`);
    console.log(`需要删除的非5A级景区: ${toDelete.length} 个`);

    if (toDelete.length > 0) {
      console.log('\n将删除以下景区:');
      toDelete.forEach((spot, index) => {
        console.log(`${index + 1}. ${spot.name} (${spot.province})`);
      });

      // 删除非5A级景区
      const deleteResult = await ScenicSpot.deleteMany({
        _id: { $in: toDelete.map(s => s._id) }
      });
      
      console.log(`\n成功删除 ${deleteResult.deletedCount} 个非5A级景区`);
    } else {
      console.log('\n没有需要删除的景区');
    }

    // 验证删除后的数量
    const remainingCount = await ScenicSpot.countDocuments();
    console.log(`\n删除后数据库中共有 ${remainingCount} 个景区`);

  } catch (error) {
    console.error('错误:', error);
  } finally {
    await mongoose.connection.close();
  }
}

removeExtraSpots();
