// 智能匹配函数
function isMatch(official: typeof officialList[0], existing: any): boolean {
  // 精确匹配
  if (existing.name === official.name) return true;
  
  // 省份和城市必须匹配
  if (existing.province !== official.province) return false;
  
  // 获取关键词
  const keywords = keywordMap[official.name] || [];
  
  // 检查是否包含任何关键词
  return keywords.some(keyword => existing.name.includes(keyword));
}

async function findMissing() {
  await mongoose.connect(process.env.MONGODB_URI || 'mongodb://localhost:27017/travel-map');
  console.log('MongoDB 连接成功');

  const existingSpots = await ScenicSpot.find({}, { name: 1, province: 1, city: 1 }).lean();
  console.log(`数据库中现有景区数量: ${existingSpots.length}`);

  const missingSpots: typeof officialList[0][] = [];
  const matchedIndices = new Set<number>();
  
  for (let i = 0; i < officialList.length; i++) {
    const official = officialList[i];
    let found = false;
    
    for (let j = 0; j < existingSpots.length; j++) {
      if (isMatch(official, existingSpots[j])) {
        found = true;
        matchedIndices.add(j);
        break;
      }
    }
    
    if (!found) {
      missingSpots.push(official);
    }
  }

  console.log(`\n缺失的景区数量: ${missingSpots.length}`);
  console.log('\n缺失的景区列表:');
  missingSpots.forEach((spot, i) => {
    console.log(`${i + 1}. ${spot.name} (${spot.province} ${spot.city})`);
  });

  // 保存到文件
  const fs = await import('fs');
  await fs.promises.writeFile('./missing-spots-detailed.json', JSON.stringify(missingSpots, null, 2));
  console.log('\n已保存到 missing-spots-detailed.json');
  
  console.log(`\n已匹配的景区数量: ${matchedIndices.size}`);
  console.log(`匹配率: ${((matchedIndices.size / officialList.length) * 100).toFixed(2)}%`);

  await mongoose.disconnect();
}

findMissing();
